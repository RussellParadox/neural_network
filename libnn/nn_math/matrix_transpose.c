#include "nn_math.h"

void	matrix_transpose(t_matrix *m, t_matrix *transposed)
{
	unsigned int	i;
	unsigned int	j;

	i = 0;
	while (i < transposed->row)
	{
		j = 0;
		while (j < transposed->col)
		{
			transposed->v[i][j] = m->v[j][i];
			j++;
		}
		i++;
	}
}
