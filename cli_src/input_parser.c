#include "nn_cli.h"

int	isnotspace(char c)
{
	return (!isspace(c));
}

char	**input_parser(char input[INPUT_LEN + 1])
{
	char	**cmd;

	cmd = split_context(input, isnotspace);
	if (cmd == NULL)
		return (NULL);
	return (cmd);
}
